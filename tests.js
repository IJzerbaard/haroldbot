QUnit.test("Circuit SAT tests", function(assert) {
	circuit.reset();
	var x = CFunction.argument(0);
	var y = CFunction.argument(1);
	var c = CFunction.constant(3);
	var f1 = CFunction.not(CFunction.eq(c, c));
	assert.equal(f1.sat(), false, "c != c is false");
	var f2 = CFunction.not(CFunction.eq(CFunction.add(x, y), CFunction.add(y, x)));
	assert.equal(f2.sat(), false, "x + y == y + x");
	var f3 = CFunction.eq(CFunction.add(x, c), y);
	assert.equal(f3.sat(), true, "x + 3 == y has solutions");
	var f4 = CFunction.eq(CFunction.mul(x, CFunction.constant(3)), CFunction.constant(1));
	assert.equal(f4.sat(), true, "3 has an inverse");
	var f5 = CFunction.eq(CFunction.mul(x, CFunction.constant(2)), CFunction.constant(1));
	assert.equal(f5.sat(), false, "2 does not have an inverse");
});

